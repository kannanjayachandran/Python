# Multi dimensional dictionaries and lists

lis = [1, 2, 3, 4, 5], ['a', 'b', 'c'], ["hello", "list"]
print(lis)
print(lis[0])
print(lis[0][1])
print(lis[2][1])

dict1 = {'India': {'Language': 'Hindi', 'population': '100'},
         'France': {'Language': 'French', 'population': '50'},
         'China': {'Language': 'Chinese', 'population': '200'}}

print(dict1)
